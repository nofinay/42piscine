#include <unistd.h>

void	ft_putchar(char c)
{
	write (1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	y;	

	y = 1;
	while (argc > y)
	{
		ft_putstr(argv[y]);
		ft_putchar('\n');
		y++;
	}
	return (0);
}
